// CIDR matching utility
function ipToLong(ip) {
  const parts = ip.split(".");
  if (parts.length !== 4) return null;
  let long = 0;
  for (let i = 0; i < 4; i++) {
    const octet = parseInt(parts[i], 10);
    if (isNaN(octet) || octet < 0 || octet > 255) return null;
    long = (long * 256) + octet;
  }
  return long >>> 0;
}

function parseCIDR(cidr) {
  const [ip, bits] = cidr.split("/");
  const mask = bits ? parseInt(bits, 10) : 32;
  const ipLong = ipToLong(ip);
  if (ipLong === null) return null;
  const maskLong = mask === 0 ? 0 : (~0 << (32 - mask)) >>> 0;
  return { network: (ipLong & maskLong) >>> 0, mask: maskLong };
}

function isIPInCIDR(ip, cidrParsed) {
  const ipLong = ipToLong(ip);
  if (ipLong === null) return false;
  return ((ipLong & cidrParsed.mask) >>> 0) === cidrParsed.network;
}

